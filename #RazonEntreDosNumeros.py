#En una clase hay 18 niñas y 9 niños.
#¿cuál es la razón entre niñas y niños?
#¿cuál es la razón entre niños y niñas?

Niñas=18
Niños=9

Formula1=(Niñas/Niños)

print(f"la razon entre niñas y niños es {Formula1}, es decir el numero de niñas es {Formula1} veces el numero de niños")


Formula2=(Niños/Niñas)

print(f"la razon entre niñas y niños es {Formula2}, es decir el numero de niños es {Formula2} veces el numero de niñas")


